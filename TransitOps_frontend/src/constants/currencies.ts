export const DEFAULT_CURRENCY_CODE = 'INR'

export function findCurrency(code: string): CurrencyOption {
  return (
    CURRENCY_OPTIONS.find((currency) => currency.code === code) ??
    CURRENCY_OPTIONS.find((currency) => currency.code === DEFAULT_CURRENCY_CODE)!
  )
}

export function currencySelectLabel(currency: CurrencyOption): string {
  return `${currency.code} — ${currency.name} (${currency.symbol})`
}
